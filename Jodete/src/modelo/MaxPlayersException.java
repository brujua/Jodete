package modelo;

public class MaxPlayersException extends Exception {
	
	public MaxPlayersException() {
		super();
	}

	public MaxPlayersException(String message, Throwable cause) {
		super(message, cause);
	
	}

	public MaxPlayersException(String message) {
		super(message);
		
	}

	public MaxPlayersException(Throwable cause) {
		super(cause);
	
	}

	
}
